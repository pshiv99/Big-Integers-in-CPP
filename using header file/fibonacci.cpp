#include <iostream>
#include "BigInt.h"
using namespace std;
int main() {
    BigInt first = 0, second = 1, third;
    int term;

    cout<<"Enter the term of Fibonacci series: ";
    cin>>term;

    cout<<"The term value is: "<<endl;
    if(term == 1) {
        cout<<first;
    }
    else if(term == 2) {
        cout<<second;
    }
    else {
        for(int i = 3; i <= term; i++) {
            third = first + second;
            first = second;
            second = third;
        }
        cout<<third;
    }
    return 0;
}

